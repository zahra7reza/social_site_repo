import React from "react";
import { withRouter } from "react-router-dom";
const User = ({ name, username, email, history }) => {
  const handleClick = () => {
    history.push("/${username})", {
      name,
      email,
      username,
    });
  };
  return (
    <div style={{ padding: "6px 0" }}>
      <div
        style={{ fontSize: "1.5rem", width: "min-content", cursor: "pointer" }}
        onClick={handleClick}
      >
        {username}
      </div>
      <div style={{ fontSize: "0.9rem", color: "silver" }}>{name}</div>
    </div>
  );
};

export default withRouter(User);
